
public class IFElse {

	public static void main(String[] args) {
		int a,b;
		a=10000;
		b=20000;
		
		
		if(a>b) 
		{
		System.out.println("A is greater");
		System.out.println(a);
		}
		else 
		{
		System.out.println("B is greater");
		System.out.println(b);
		}
		
		
		System.out.println("END");
		
	}

}
